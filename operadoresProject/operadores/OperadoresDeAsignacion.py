#Ejemplos de operadores de asignacion
var1 = 5
var2 = 12
var3 = 8
var4 = 14
var5 = 6
var6 = 8

#SUMA
var1 += 3 # var1 = var1 + 3 asignacion con suma
print(f"Asignacion con suma: {var1}")

#RESTA
var1 -= 3 # var1 = var1 + 3 asignacion con resta
print(f"Asignacion con resta: {var1}")

#MULTIPLICACION
var1 *= 3 # var1 = var1 + 3 asignacion con multiplicacion
print(f"Asignacion con multiplicacion: {var1}")

#DIVISION
var1 /= 3 # var1 = var1 + 3 asignacion con division
print(f"Asignacion con division: {var1}")

#RESIDUO
var1 %= 3 # var1 = var1 + 3 asignacion con residuo
print(f"Asignacion con residuo: {var1}")

#DIVISION ENTERA
var1 //= 3 # var1 = var1 + 3 asignacion con division entera
print(f"Asignacion con division entera: {var1}")